# matematika

x = 10
y = 5

print(x + y)   # penjumlahan
print(x - y)   # pengurangan
print(x * y)   # perkalian
print(x ** y)  # perpangkatan
print(x / y)   # pembagian dengan float return value
print(x // y)  # pembagian dengan integer return value
print(x % y)   # modulus

x += 5
print(x)       # 15

x -= 5
print(x)       # 10
