# perulangan for

# perulangan for untuk string
name = "Rizqi Assyaufi"

for n in name:
    print(n)


# perulangan for untuk list
numbers = [1, 2, 3, 4, 5, 6]

for number in numbers:
    print(number)
    print(number * 10)


# perulangan for dengan function range()
for number in range(10):
    print(number)

for number in range(1, 11):
    print(number)

for number in range(1, 11, 2):
    print(number)
